            <p class="Texto">
                A ética tornou-se, assim, um importante assunto de interesse público que refletiu na variedade de tentativas de “limpar” a vida pública, incluindo a revisão dos sistemas de gestão e comportamento dos funcionários. Tal processo de limpeza, naturalmente, exige uma combinação de diversos mecanismos e agentes desse processo, que precisam estar interrelacionados, a fim de garantir o seu êxito. Ou seja, o PUMA reconhece que a prevenção da corrupção é tão complexa quanto a própria corrupção. De forma que se faz necessário tanto identificar o corrupto como também “os mecanismos e as estruturas de ação que conduzem à corrupção”.
            </p>
            <p class="Texto">
                A proposta de infraestrutura ética da OCDE integra oito componentes: o compromisso político, o marco legal, os mecanismos de responsabilidade, os códigos de conduta, a socialização profissional, as condições do serviço público, os órgãos de coordenação e participação e votações públicas. Por sua vez, estes componentes são agrupados em outras três funções principais: Controle, Orientação e Gestão.
