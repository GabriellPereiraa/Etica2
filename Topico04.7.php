            <p class="Texto">
                Nos dias 15 e 16 de junho de 1998, o <span style="color:#FC6B30;">Centro Latino-americano de Administração para o Desenvolvimento </span>(CLAD – Centro Latinoamericano de Administración para el Desarrollo), com o apoio da <span style="color:#FC6B30;">Agência Espanhola de Cooperação Internacional para o Desenvolvimento </span>(AECID - Agencia Española de Cooperación Internacional para el Desarrollo), realizou em Santa Cruz de la Sierra, na Bolívia, um Fórum Ibero-americano com o propósito de: examinar as formas mais proeminentes e as consequências sociais da corrupção, e de explorar as formas e métodos que atualmente têm sido adotados no mundo para combatê-la e, dessa maneira, buscar reduzir sua presença e atividade na região. Um dos resultados desse fórum foi a criação da Rede de Instituições de Combate à Corrupção e Resgate da Ética Pública
            </p>
            <p class="Texto">
                – RICOREP.
            </p>
            <p class="Texto">
                A RICOREP reúne diversas instituições governamentais e nãogovernamentais (instituições de investigação com sede nos Estados Unidos e na América Latina), que possuem as funções de combater a corrupção em todas as suas manifestações e de resgatar os princípios éticos no exercício da função pública. Essa rede está formada por pessoas que desenvolvem estudos e investigações sobre essa temática. Entre os seus objetivos, incluem-se: o intercâmbio de informações sobre a estrutura e organização de cada uma das instituições referidas e as normas vigentes em matéria de enfrentamento da corrupção. Entre outras atividades, promove eventos periodicamente que estejam relacionados a essa matéria, realiza atividades de formação, investigação, consultoria técnica e difusão de publicações especializadas.
            </p>
            <p class="Texto">
